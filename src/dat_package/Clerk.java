package dat_package;

public class Clerk {
    private String name;
    private String phoneNb;

    public Clerk(String name, String phoneNb) {
        this.name = name;
        this.phoneNb = phoneNb;
    }

    public String getName() {
        return name;
    }

    public String getPhoneNb() {
        return phoneNb;
    }
}
